
public class Exemplo5 {

	public static void main(String[] args) {
		int numInteiro = 10;
		float numFrac = 50.8f;
		double numFrac2 = 20.0d;
		
		System.out.println(numInteiro + numInteiro);
		System.out.println(numInteiro + numFrac2);
		System.out.println(numFrac - numFrac2);
		System.out.println(numInteiro * numInteiro);
		System.out.println(numInteiro / numInteiro);
		System.out.println(numInteiro % numInteiro);
		
		char letra1 = 'D';
		char letra2 = 'I';
		//ASCII
		System.out.println("char: "+letra1 + letra2);
		
		String letraA = "A";
		String letraB = "B";
		System.out.println(letraA + letraB);
	}

}
